
// This function demonstrates the use of if-else statements in Rust.
pub fn if_statements() {
    let number = 5;

    if number < 10 {
        println!("The number is less than 10");
    } else if number == 10 {
        println!("The number is equal to 10");
    } else {
        println!("The number is greater than 10");
    }
}